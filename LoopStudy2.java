import java.util.*;
public class LoopStudy2
{
public static void main(String args[])
{
//While LoopStudy
int n=5;
while(n>0)
{
	System.out.println("Hello");
	n--;
}
System.out.println("After the while loop n is"+ n);

//Do-While LOOP
n=5;
do
{
	System.out.println(n);
	n--;
	}
	while(n>0);
	System.out.println("After the do-while loop n is "+ n);
	
}
}
